char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	{
		while (s[i])
		{
			if (s[i] == c)
			{
				return ((char *)s);
			}
			s++;
		}
		if (c == '\0')
		{
			return ((char *)s);
		}
	}
	return (0);
}